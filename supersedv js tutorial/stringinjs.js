// strings in javascript!...
// strings in js can insert in a single / double quote or a backtick.

const myName = 'My name is'
const name = 'lemuel'
console.log(`${myName}: ${name}`);
// output is My name is: lemuel

const total = Math.round(599 + 295) / 100;
console.log(`Total cost: $${total}`);
console.log('Thank you, come again!');
 

// string concatenation in js with single quote and a + sign
const firstName = 'lemuel'
const secondName = 'shayne'
console.log('our name is ' + firstName + ' and ' + secondName);
// output is our name is lemuel and shayne
