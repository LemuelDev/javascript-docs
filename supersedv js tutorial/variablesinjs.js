//  welcome to variables in javascript
//  3 ways to create a variable
//  const --- constant variable / static
// let ----- can change the value of the variable
//  var --- the old school one to declare a variable

const name = 'lemuel_pogi';
console.log(`My name is: ${name}`);
// output is My name is: lemuel_pogi

const cost = 5 + 6 + 9;
console.log(`Cost of food: $${cost}`);
// output is Cost of food: $20

const costTax = cost * .10;
console.log(`Tax of your order: $${costTax}`);
// output is Tax of your order: $2